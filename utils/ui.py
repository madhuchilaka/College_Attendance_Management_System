import os


# ==========================================
# Clear Screen
# ==========================================

def clear_screen():

    os.system("cls" if os.name == "nt" else "clear")


# ==========================================
# Header
# ==========================================

def print_header(title):

    print("\n")

    print("╔" + "═" * 58 + "╗")

    print(f"║{title.center(58)}║")

    print("╚" + "═" * 58 + "╝")


# ==========================================
# Divider
# ==========================================

def print_divider():

    print("─" * 60)


# ==========================================
# Success Message
# ==========================================

def success(message):

    print(f"\n✅ {message}")


# ==========================================
# Error Message
# ==========================================

def error(message):

    print(f"\n❌ {message}")


# ==========================================
# Warning Message
# ==========================================

def warning(message):

    print(f"\n⚠ {message}")


# ==========================================
# Information
# ==========================================

def info(message):

    print(f"\nℹ {message}")